#!/bin/bash
# =============================================================================
# VendorBoss API вЂ” One-Time Server Setup Script
# Run this ONCE on your Ubuntu server to prepare it for deployments.
#
# Usage:
#   ssh into your server, then:
#   curl -o setup.sh https://raw.githubusercontent.com/YOUR_USERNAME/YOUR_REPO/main/vendorboss-api/scripts/server_setup.sh
#   chmod +x setup.sh && sudo bash setup.sh
#
#   OR copy it over with scp and run it.
# =============================================================================

set -e

APP_USER="vendorboss"
APP_DIR="/opt/vendorboss/api"
VENV="$APP_DIR/venv"
PYTHON="python3"

echo "============================================="
echo " VendorBoss API вЂ” Server Setup"
echo "============================================="

# в”Ђв”Ђ 1. System packages в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџ“¦ Installing system packages..."
apt-get update -qq
apt-get install -y -qq \
    python3 \
    python3-pip \
    python3-venv \
    python3-dev \
    postgresql \
    postgresql-contrib \
    nginx \
    git \
    curl \
    rsync \
    build-essential \
    libpq-dev

# в”Ђв”Ђ 2. Create app user в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџ‘¤ Creating app user..."
if ! id "$APP_USER" &>/dev/null; then
    useradd --system --create-home --shell /bin/bash "$APP_USER"
    echo "   Created user: $APP_USER"
else
    echo "   User $APP_USER already exists"
fi

# в”Ђв”Ђ 3. Create app directory в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџ“Ѓ Setting up app directory..."
mkdir -p "$APP_DIR"
chown -R "$APP_USER:$APP_USER" /opt/vendorboss

# в”Ђв”Ђ 4. Python virtual environment в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџђЌ Creating Python virtual environment..."
sudo -u "$APP_USER" $PYTHON -m venv "$VENV"
echo "   Venv created at $VENV"

# в”Ђв”Ђ 5. PostgreSQL setup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџ—„пёЏ  Setting up PostgreSQL..."
systemctl start postgresql
systemctl enable postgresql

# Create DB user and database (prompts for password)
echo ""
echo "   вљ пёЏ  You'll be prompted to set a password for the vendorboss database user."
echo "      Save this вЂ” you'll need it in your .env file."
echo ""
sudo -u postgres psql <<SQL
DO \$\$
BEGIN
    IF NOT EXISTS (SELECT FROM pg_catalog.pg_roles WHERE rolname = 'vendorboss') THEN
        CREATE ROLE vendorboss WITH LOGIN PASSWORD 'CHANGE_ME_NOW';
    END IF;
END
\$\$;

CREATE DATABASE vendorboss_db OWNER vendorboss;
GRANT ALL PRIVILEGES ON DATABASE vendorboss_db TO vendorboss;
SQL
echo "   вњ… Database 'vendorboss_db' created with user 'vendorboss'"
echo "   вљ пёЏ  Change the password: sudo -u postgres psql -c \"ALTER USER vendorboss PASSWORD 'your-strong-password';\""

# в”Ђв”Ђ 6. Environment file в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "вљ™пёЏ  Creating .env file..."
if [ ! -f "$APP_DIR/.env" ]; then
    SECRET=$(python3 -c "import secrets; print(secrets.token_urlsafe(32))")
    cat > "$APP_DIR/.env" <<ENV
# VendorBoss API вЂ” Production Environment
# Generated by server_setup.sh вЂ” edit as needed

DATABASE_URL=postgresql://vendorboss:CHANGE_ME_NOW@localhost:5432/vendorboss_db
SECRET_KEY=$SECRET
SCANBOSS_API_KEY=your-scanboss-key-here
PORT=8000
ENVIRONMENT=production
ENV
    chown "$APP_USER:$APP_USER" "$APP_DIR/.env"
    chmod 600 "$APP_DIR/.env"
    echo "   вњ… .env created at $APP_DIR/.env"
    echo "   вљ пёЏ  Edit it now: nano $APP_DIR/.env"
else
    echo "   .env already exists вЂ” skipping"
fi

# в”Ђв”Ђ 7. Systemd service в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "вљ™пёЏ  Installing systemd service..."
cat > /etc/systemd/system/vendorboss.service <<SERVICE
[Unit]
Description=VendorBoss API
After=network.target postgresql.service
Wants=postgresql.service

[Service]
Type=simple
User=$APP_USER
WorkingDirectory=$APP_DIR
EnvironmentFile=$APP_DIR/.env
ExecStart=$VENV/bin/uvicorn main:app --host 0.0.0.0 --port 8000 --workers 2
Restart=always
RestartSec=5
StandardOutput=journal
StandardError=journal
SyslogIdentifier=vendorboss

[Install]
WantedBy=multi-user.target
SERVICE

systemctl daemon-reload
systemctl enable vendorboss
echo "   вњ… Service installed and enabled (not started yet вЂ” deploy first)"

# в”Ђв”Ђ 8. Nginx reverse proxy в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџЊђ Configuring Nginx..."
cat > /etc/nginx/sites-available/vendorboss <<NGINX
server {
    listen 80;
    server_name _;  # Replace with your domain or IP if needed

    location / {
        proxy_pass http://127.0.0.1:8000;
        proxy_http_version 1.1;
        proxy_set_header Upgrade \$http_upgrade;
        proxy_set_header Connection 'upgrade';
        proxy_set_header Host \$host;
        proxy_set_header X-Real-IP \$remote_addr;
        proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
        proxy_cache_bypass \$http_upgrade;
        proxy_read_timeout 300;
    }
}
NGINX

ln -sf /etc/nginx/sites-available/vendorboss /etc/nginx/sites-enabled/vendorboss
rm -f /etc/nginx/sites-enabled/default
nginx -t && systemctl restart nginx && systemctl enable nginx
echo "   вњ… Nginx configured and running"

# в”Ђв”Ђ 9. Sudoers вЂ” allow runner to restart service without password в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "рџ”ђ Configuring sudoers for deployment..."
cat > /etc/sudoers.d/vendorboss-deploy <<SUDOERS
# Allow the GitHub Actions runner user to restart the vendorboss service
# The runner will run as the user you register it with (often 'ubuntu' or your login)
%sudo ALL=(ALL) NOPASSWD: /usr/bin/systemctl restart vendorboss
%sudo ALL=(ALL) NOPASSWD: /usr/bin/systemctl status vendorboss
%sudo ALL=(ALL) NOPASSWD: /usr/bin/systemctl is-active vendorboss
SUDOERS
chmod 0440 /etc/sudoers.d/vendorboss-deploy
echo "   вњ… Sudoers configured"

# в”Ђв”Ђ 10. GitHub Actions Runner instructions в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo "============================================="
echo " вњ… Server setup complete!"
echo "============================================="
echo ""
echo "NEXT STEPS:"
echo ""
echo "1. Edit your .env file:"
echo "   nano $APP_DIR/.env"
echo "   (Update DATABASE_URL password and SCANBOSS_API_KEY)"
echo ""
echo "2. Install the GitHub Actions self-hosted runner:"
echo "   - Go to your GitHub repo"
echo "   - Settings в†’ Actions в†’ Runners в†’ New self-hosted runner"
echo "   - Choose Linux x64"
echo "   - Follow the commands GitHub shows you"
echo "   - When asked for labels, just press Enter (default is fine)"
echo "   - Install as a service: sudo ./svc.sh install && sudo ./svc.sh start"
echo ""
echo "3. Push to main to trigger your first deployment!"
echo ""
echo "4. Check logs anytime:"
echo "   journalctl -u vendorboss -f"
echo ""
